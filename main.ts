function showScore () {
    if (coinAmount_sprite) {
        coinAmount_sprite.setFlag(SpriteFlag.Invisible, false)
    } else {
        coinAmount_sprite = textsprite.create("0", 0, 15)
        coinAmount_sprite.setBorder(1, 0)
        coinAmount = 0
        coinAmount_sprite.setFlag(SpriteFlag.RelativeToCamera, true)
        coinAmount_sprite.setPosition(26, 23)
        coinAmount_sprite.scale = 2
    }
}
